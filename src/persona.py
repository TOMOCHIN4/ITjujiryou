"""ペルソナ漏れ検査の共通モジュール。

クライアントへ届くテキスト (deliver の delivery_message, send_message の content)
に事務所内ペルソナ用語が混入していないか検査する。

愛帝十字陵 v3.1 世界観: 5 名中 4 名が北斗の拳の世界からの転生者。
クライアントには「普通の現代的な Web 受託事務所」として振る舞うため、
前世名・北斗用語・社内符丁・各キャラの口癖が外向け文書に漏れることを禁止する。
"""
from __future__ import annotations

FORBIDDEN_TERMS: list[str] = [
    # 称号 (称号でクライアントを呼ばない)
    "帝王", "聖帝", "拳王", "愛帝",
    # 前世名 (社員の前世を外部に知らせない)
    "サウザー", "ラオウ", "トキ", "ケンシロウ", "オウガイ", "お師さん",
    # 流派・北斗用語
    "南斗", "鳳凰拳", "北斗", "北斗神拳", "医療北斗", "北斗SRE拳", "北斗 SRE 拳",
    # 蔑称・呼称 (社内のみ)
    "下郎", "雑兵", "アリの反逆",
    # サザンの口癖・聖帝口調
    "ふん、", "退かぬ", "媚びぬ", "省みぬ",
    "ひかぬ", "こびぬ", "かえりみぬ",
    "愛などいらぬ",
    # 新世界観の社訓・経営ポエム (クライアント宛では出さない)
    "制圧前進", "わが社にあるのはただ制圧前進",
    # 各キャラの社内符丁・決め台詞 (クライアント納品物には混入させない)
    "死兆星",
    "アタタタタタッ",
    "お前はもう…済んでいる", "お前はもう・・・済んでいる", "お前はもう済んでいる",
    "我が文に勝るものなし",
    "わが提案に一片の悔いなし",
    "天に帰る",
    "激流を制するのは静水",
    "意志を放棄した文章は文章にあらず",
    # 「これは…愛か？」「温もり」はサザン独白の境界事象。社外には出さない
    "これは…愛か", "これは・・・愛か",
    # Phase 2 申し送り 2: XML/HTML 風の内部 artifact タグ (Phase 1 スモークで `</delivery_message>` 漏れ観察)
    "<delivery_message>", "</delivery_message>",
    "<email>", "</email>",
    "<message>", "</message>",
]


_ALLOWED_PHRASES: tuple[str, ...] = (
    # Phase 2: 「愛帝十字陵」は社名 (株式会社 愛帝十字陵)。「愛帝」が客への称号として
    # 使われた場合のみブロックしたいので、社名の中に出る「愛帝」は予め mask して
    # スキャンする。スキャン前に空文字へ置換するだけ。順序敏感ではない。
    "愛帝十字陵",
)


def find_forbidden_terms(text: str) -> list[str]:
    if not text:
        return []
    scanned = text
    for phrase in _ALLOWED_PHRASES:
        scanned = scanned.replace(phrase, "")
    return [t for t in FORBIDDEN_TERMS if t in scanned]
